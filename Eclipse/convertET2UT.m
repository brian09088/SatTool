function delta_T = convertET2UT(y)

if y < -500
    u = (y-1820)/100;
    delta_T = -20 + 32 * u^2;
end
if y >= -500 && y < 500
    u = y/100;
    delta_T = 10583.6 - 1014.41 * u + 33.78311 * u^2 - 5.952053 * u^3 ...
		- 0.1798452 * u^4 + 0.022174192 * u^5 + 0.0090316521 * u^6;
end
if y >= 500 && y < 1600
    u = (y-1000)/100;
    delta_T = 1574.2 - 556.01 * u + 71.23472 * u^2 + 0.319781 * u^3 ...
		- 0.8503463 * u^4 - 0.005050998 * u^5 + 0.0083572073 * u^6;
end
if y >= 1600 && y < 1700
    t = y - 1600;
    delta_T = 120 - 0.9808 * t - 0.01532 * t^2 + t^3 / 7129;
end
if y >= 1700 && y < 1800
    t = y - 1700;
    delta_T = 8.83 + 0.1603 * t - 0.0059285 * t^2 + 0.00013336 * ...
         t^3 - t^4 / 1174000;
end
if y >= 1800 && y < 1860
    t = y - 1800;
    delta_T = 13.72 - 0.332447 * t + 0.0068612 * t^2 + 0.0041116 * ...
        t^3 - 0.00037436 * t^4 + 0.0000121272 * t^5 - 0.0000001699 * ...
        t^6 + 0.000000000875 * t^7;
end
if y >= 1860 && y<1900
    t = y - 1860;
    delta_T = 7.62 + 0.5737 * t - 0.251754 * t^2 + 0.01680668 * t^3 ...
		-0.0004473624 * t^4 + t^5 / 233174;
end
if y >= 1900 && y<1920
    t = y - 1900;
    delta_T = -2.79 + 1.494119 * t - 0.0598939 * t^2 + 0.0061966 * ...
        t^3 - 0.000197 * t^4;
end
if y >= 1920 && y<1941
    t = y - 1920;
    delta_T = 21.20 + 0.84493*t - 0.076100 * t^2 + 0.0020936 * t^3;
end
if y >= 1941 && y<1961
    t = y - 1950;
    delta_T = 29.07 + 0.407*t - t^2/233 + t^3 / 2547;
end
if y >= 1961 && y<1986
    t = y - 1975;
    delta_T = 45.45 + 1.067*t - t^2/260 - t^3 / 718;
end
if y >= 1986 && y<2005
    t = y - 2000;
    delta_T = 63.86 + 0.3345 * t - 0.060374 * t^2 + 0.0017275 * ...
    t^3 + 0.000651814 * t^4 + 0.00002373599 * t^5;
end
if y >= 2005 && y < 2050
	t = y - 2000;
    delta_T = 62.92 + 0.32217 * t + 0.005589 * t^2;
end
if y >= 2050 && y < 2150
    delta_T = -20 + 32 * ((y-1820)/100)^2 - 0.5628 * (2150 - y);
end
if y >= 2150
    u = (y-1820)/100;
    delta_T = -20 + 32 * u^2;
end

end